package com.example.qurbancare;

public class Mudhohi {
    private String nama, jenis_hewan, paket, jenis_kelamin, umur, harga, gambar;

    // Konstruktor kosong wajib untuk Firebase
    public Mudhohi() {}

    // Getter (Klik kanan > Generate > Getter)
    public String getNama() { return nama; }
    public String getJenis_hewan() { return jenis_hewan; }
    public String getPaket() { return paket; }
    public String getJenis_kelamin() { return jenis_kelamin; }
    public String getUmur() { return umur; }
    public String getHarga() { return harga; }
    public String getGambar() { return gambar; }

}
